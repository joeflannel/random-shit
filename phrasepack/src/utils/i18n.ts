let currentLang: UILang = 'en'

export function setUILanguage(lang: UILang): void {
  currentLang = lang
  try {
    localStorage.setItem(UI_LANGUAGE_KEY, lang)
  } catch {}
}

export function getUILanguage(): UILang {
  try {
    const saved = localStorage.getItem(UI_LANGUAGE_KEY) as UILang | null
    if (saved && messages[saved]) return saved
  } catch {}
  return currentLang
}

export function hasSavedUILanguage(): boolean {
  try {
    return localStorage.getItem(UI_LANGUAGE_KEY) != null
  } catch {
    return false
  }
}

export function t(key: string): string {
  const lang = getUILanguage()
  const dict = messages[lang] || messages.en
  return dict[key] || messages.en[key] || key
}

// New function to get phrase translation in user's UI language
export function getPhraseTranslation(phraseKey: string): string {
  const lang = getUILanguage()
  const translations = phraseTranslations[lang] || phraseTranslations.en
  return translations[phraseKey] || phraseTranslations.en[phraseKey] || phraseKey
}

// New function to get sentence translation in user's UI language
export function getSentenceTranslation(sentenceKey: string): string {
  const lang = getUILanguage()
  const translations = sentenceTranslations[lang] || sentenceTranslations.en
  return translations[sentenceKey] || sentenceTranslations.en[sentenceKey] || sentenceKey
}

